  likeicon.setAttribute("class", "likeicon");
  likeicon.setAttribute("id", "button" + i);
  div.appendChild(likeicon);
  let buttonfunction = (a) => {
    a;
  };
  buttonfunction("button" + [i]);

  let numberOfLikes = document.createElement("span");
