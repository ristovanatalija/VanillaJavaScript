let jsonIndex = 0;

document.getElementById("loadmore").onclick = function() {
  let collection = [];
  for (let i = 0; (i < 4) && (json.length > jsonIndex); i++) {
      collection.push(json[jsonIndex++]);
  }
  appendData(collection);
  if (jsonIndex >= json.length) this.style.display = "none";
}